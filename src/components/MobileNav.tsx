'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'

const navItems = [
  { href: '/', label: 'Explore', icon: SearchIcon },
  { href: '/wishlists', label: 'Wishlists', icon: HeartIcon },
  { href: '/trips', label: 'Trips', icon: LogoIcon },
  { href: '/inbox', label: 'Inbox', icon: MessageIcon },
  { href: '/profile', label: 'Profile', icon: UserIcon },
]

export default function MobileNav() {
  const pathname = usePathname()

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 border-t border-[#EBEBEB] bg-white md:hidden">
      <div className="flex items-center justify-around py-2">
        {navItems.map((item) => {
          const isActive = pathname === item.href
          const Icon = item.icon
          return (
            <Link
              key={item.href}
              href={item.href}
              className="flex flex-col items-center gap-1 px-3 py-1"
            >
              <Icon
                className={`h-6 w-6 ${
                  isActive ? 'text-[#FF5A5F]' : 'text-[#767676]'
                }`}
              />
              <span
                className={`text-xs ${
                  isActive
                    ? 'font-medium text-[#FF5A5F]'
                    : 'text-[#767676]'
                }`}
              >
                {item.label}
              </span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}

